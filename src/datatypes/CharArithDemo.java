package datatypes;

public class CharArithDemo {
    public static void main (String args []){

        char ch;

        ch = 'X';
        System.out.println ("ch contais " + ch);

        ch++ ; // increment ch
        System.out.println("ch is now " + ch);

        ch = 84; // give ch the value Z
        System.out.println("ch is now " + ch);

    }
}
